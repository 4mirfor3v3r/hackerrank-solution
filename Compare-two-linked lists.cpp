bool compare_lists(SinglyLinkedListNode* head1, SinglyLinkedListNode* head2) {
    SinglyLinkedListNode* p;
    SinglyLinkedListNode* q;
    p=head1;
    q=head2;
    while(p != NULL && q != NULL){
        if(p->data != q->data){
            return false;
        }
        p=p->next;
        q=q->next;
    }
    if(p == NULL && q==NULL){
        return true;
    }else{
        return false;
    }
}
