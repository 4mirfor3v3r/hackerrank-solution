bool has_cycle(SinglyLinkedListNode* head) {
    SinglyLinkedListNode *p;
    p=head;
    while (p!=NULL) {
        if(p->data== -11){
            return true;
        }
        p->data=-11;
        p=p->next;
    }
    return false;
}

// Using Floyd Warshal algorithm
bool anotherHasCycleSolution(SinglyLinkedListNode* head) {
    SinglyLinkedListNode* fast=head, *slow=head;
    while(slow != NULL && fast != NULL && fast->next !=NULL){
        slow=slow->next;
        fast=fast->next->next;
        if(slow == fast){
            return true;
        }
    }
    return false;
}